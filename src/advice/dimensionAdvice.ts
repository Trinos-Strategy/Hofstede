/**
 * 권력거리(PDI) - 회의/아이디어 제안용
 */
export function adviceForMeeting_PDI(pdi: number): string[] {
  if (pdi >= 60) {
    return [
      '공개 회의에서는 윗사람의 체면을 먼저 고려하는 것이 안전합니다.',
      '새 아이디어는 회의 전에 1:1로 상사와 상의한 뒤 공식 자리에서 꺼내는 방식을 추천합니다.',
    ];
  }
  if (pdi <= 40) {
    return [
      '직급에 상관없이 바로 아이디어를 말하는 것이 자연스러운 문화입니다.',
      '상사 의견과 달라도, 근거만 분명하면 회의 중에 바로 제안해도 괜찮습니다.',
    ];
  }
  return [
    '형식적인 위계는 있으나, 적절한 선에서 질문과 토론도 허용됩니다.',
    '중요한 이견은 회의 전·후에 정리하고, 회의 중에는 질문 형식으로 부드럽게 제기해 보세요.',
  ];
}

